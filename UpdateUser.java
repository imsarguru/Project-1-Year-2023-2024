    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String userRole = (String) comboName.getSelectedItem();
        String name = txtName.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = DOB.getDate();
        String dob = "";
        if (date != null) {
            dob = dFormat.format(date);
        }
        String mobileNumber = txtMobileNumber.getText();
        String email = txtEmail.getText();
        String username = txtUsername.getText();
        String address = txtAddress.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name field is Required");
        } else if (dob.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Date of Birth field is Required");
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Email field is Required");
        } else if (!email.matches(emailPattern)) {
            JOptionPane.showMessageDialog(null, "Email field is Invalid");
        } else if (mobileNumber.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Mobile Number field is Required");
        } else if (!mobileNumber.matches(mobileNumberPattern) || mobileNumber.length() != 10) {
            JOptionPane.showMessageDialog(null, "Mobile Number field is Invalid");
        } else if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "User field is Required");
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Address field is Required");
        } else {
            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy?useSSL=false", "root", "12345");
                PreparedStatement ps = con.prepareStatement("UPDATE appuser SET userRole=?, name=?, dob=?, email=?, moblieNumber=?, address=? WHERE username=?");
                ps.setString(1, userRole);
                ps.setString(2, name);
                ps.setString(3, dob);
                ps.setString(4, email);
                ps.setString(5, mobileNumber);
                ps.setString(6, address);
                ps.setString(7, username);

                int rowsUpdated = ps.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(null, "User Updated Successfully");
                    setVisible(false);
                    new UpdateUser().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to update user");
                }

                con.close(); // Close the connection
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

