    private void txtNoUnitKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNoUnitKeyReleased
        // TODO add your handling code here:
        String noOfUnits = txtNoUnit.getText();
        if (!noOfUnits.equals("")) {
            String price = txtUnit.getText();
            if (!noOfUnits.matches(numberPattern)) {
                JOptionPane.showMessageDialog(null, "Number of Unit field is Invalid.");
            }
            int totalPrice = Integer.parseInt(noOfUnits) * Integer.parseInt(price);
            txtTotalPrice.setText(String.valueOf(totalPrice));
        } else {
            txtTotalPrice.setText("");
        }
    }//GEN-LAST:event_txtNoUnitKeyReleased

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String noOfUnits = txtNoUnit.getText();
        String uniqueId = txtUniqueId.getText();

        if (!noOfUnits.equals("") && !uniqueId.equals("")) {
            String name = txtName.getText();
            String company = txtComp.getText();
            String pricePerUnit = txtUnit.getText();
            String totalPrice = txtTotalPrice.getText();
            int checkStock = 0;
            int checkMedicineAlreadyExistInCart = 0;

            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy?useSSL=true&allowPublicKeyRetrieval=true", "root", "12345");
                PreparedStatement ps = con.prepareStatement("SELECT * FROM medicines WHERE uniqueId = ?");
                ps.setString(1, uniqueId);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    if (rs.getInt("quantity") >= Integer.parseInt(noOfUnits)) {
                        checkStock = 1;
                    } else {
                        JOptionPane.showMessageDialog(null, "Medicines Out Of Stock. Only " + rs.getInt("quantity") + " Left");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Medicine with ID " + uniqueId + " not found.");
                }

                con.close();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "An error occurred while fetching medicine information.");
            }

            if (checkStock == 1) {
                DefaultTableModel dtm = (DefaultTableModel) cartTable.getModel();
                boolean medicineAlreadyExists = false;

                if (cartTable.getRowCount() != 0) {
                    for (int i = 0; i < cartTable.getRowCount(); i++) {
                        if (Integer.parseInt(dtm.getValueAt(i, 0).toString()) == Integer.parseInt(uniqueId)) {
                            checkMedicineAlreadyExistInCart = 1;
                            medicineAlreadyExists = true;
                            JOptionPane.showMessageDialog(null, "Medicine already exists in the Cart");
                            break; // No need to continue checking if it already exists
                        }
                    }
                }

                if (!medicineAlreadyExists) {
                    dtm.addRow(new Object[]{uniqueId, name, company, pricePerUnit, noOfUnits, totalPrice});
                    finalTotalPrice += Integer.parseInt(totalPrice);
                    txtRs.setText(String.valueOf("Rs ₹: " + finalTotalPrice));
                    JOptionPane.showMessageDialog(null, "Added Successfully");
                }

                //clearMedicineFields();
            } else {
                JOptionPane.showMessageDialog(null, "Medicine not in stock or not found.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No of Units and Medicine ID Field are required.");
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void cartTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cartTableMouseClicked
        // TODO add your handling code here:
        int index = cartTable.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do You want to Remove this Medicine", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0 && index != -1) { // Make sure a row is selected and the user clicked "Yes"
            DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
            String total = model.getValueAt(index, 5).toString();
            finalTotalPrice -= Integer.parseInt(total);
            txtRs.setText("Rs ₹: " + finalTotalPrice);
            model.removeRow(index); // Remove the selected row from the table
        }

    }//GEN-LAST:event_cartTableMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here
        if (finalTotalPrice != 0) {
            billId = getUniqueId("Bill - ");

            DefaultTableModel dtm = (DefaultTableModel) cartTable.getModel();
            for (int i = 0; i < cartTable.getRowCount(); i++) {
                try {
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy?useSSL=false", "root", "12345");
                    Statement st = con.createStatement();
                    int medicineId = Integer.parseInt(dtm.getValueAt(i, 0).toString());
                    int quantity = Integer.parseInt(dtm.getValueAt(i, 4).toString());
                    st.execute("UPDATE medicines SET quantity = quantity - " + quantity + " WHERE uniqueId = " + medicineId);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }

            try {
                SimpleDateFormat myFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                Calendar cal = Calendar.getInstance();
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy?useSSL=false", "root", "12345");
                PreparedStatement ps = con.prepareStatement("INSERT INTO bill(billId, billDate, totalPaid, generatedBy) VALUES (?, ?, ?, ?)");
                ps.setString(1, billId);
                ps.setString(2, myFormat.format(cal.getTime()));
                ps.setInt(3, finalTotalPrice);
                ps.setString(4, username);
                ps.executeUpdate();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

            // Create Bill
            com.itextpdf.text.Document doc = new com.itextpdf.text.Document();

            try {
                PdfWriter.getInstance(doc, new FileOutputStream(PharmacyUtils.billPath + "" + billId + ".pdf"));
                doc.open();
                Paragraph pharmacyName = new Paragraph("                                       Pharmacy Management System\n");
                doc.add(pharmacyName);
                Paragraph starLine = new Paragraph("                        ***********************************************************************");
                doc.add(starLine);
                Paragraph details = new Paragraph("\tBill ID : " + billId + "\nDate : " + new Date() + "\nTotal Paid : " + finalTotalPrice);
                doc.add(details);
                doc.add(starLine);
                PdfPTable tb1 = new PdfPTable(6);
                tb1.addCell("Medicine ID");
                tb1.addCell("Name");
                tb1.addCell("Company Name");
                tb1.addCell("Price per Unit");
                tb1.addCell("No. of Units");
                tb1.addCell("Sub Total Price");
                for (int i = 0; i < cartTable.getRowCount(); i++) {
                    String a = cartTable.getValueAt(i, 0).toString();
                    String b = cartTable.getValueAt(i, 1).toString();
                    String c = cartTable.getValueAt(i, 2).toString();
                    String d = cartTable.getValueAt(i, 3).toString();
                    String e = cartTable.getValueAt(i, 4).toString();
                    String f = cartTable.getValueAt(i, 5).toString();
                    tb1.addCell(a);
                    tb1.addCell(b);
                    tb1.addCell(c);
                    tb1.addCell(d);
                    tb1.addCell(e);
                    tb1.addCell(f);
                }
                doc.add(tb1);
                doc.add(starLine);
                Paragraph thanksMsg = new Paragraph("Thank You, Please Visit Again.");
                doc.add(thanksMsg);
                doc.close();
                setVisible(false);
                new SellMedicine(username).setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please add some medicine to cart");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

