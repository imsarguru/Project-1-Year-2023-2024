    public void medicineName(String searchString) {
        DefaultTableModel model = (DefaultTableModel) medicinesTable.getModel();
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy?useSSL=true&allowPublicKeyRetrieval=true", "root", "12345");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM medicines where name like '" + searchString + "%' or uniqueId like '" + searchString + "%'");
            model.setRowCount(0); // Clear existing rows in the table
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("uniqueId") + " - " + rs.getString("name")});
            }
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

